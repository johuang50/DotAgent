	private static void updateComboBox(int init) {
		int total = output.size();
		jComboBox1.removeAllItems();

		for (int i = 0; i < total + 1; i++) {
			jComboBox1.addItem("" + (init + i));
		}
		if (total != 0) {
			jComboBox1.setSelectedIndex(total);
		}

	}

